def get_curve_target(curve: str, config: str, year: int, month: int) -> float | None:
    """
    Get ELC target for a specific curve and config at a given month.

    Args:
        curve: Curve name (A1, B1, B2, C1, C2, D0, D1)
        config: Config key (e.g., 'Y6CP_192GB_7500MTPS')
        year: Calendar year
        month: Calendar month (1-12)

    Returns:
        Target percentage or None if not found
    """
    if curve not in ELC_CURVES:
        return None

    curve_data = ELC_CURVES[curve]
    if config not in curve_data:
        return None

    schedule = curve_data[config]

    if (year, month) in schedule:
        return schedule[(year, month)]

    return schedule.get('default')


def get_curve_history_for_config(config: str) -> list[dict]:
    """
    Get target history across all curves for a specific config.

    Args:
        config: Config key (e.g., 'Y6CP_192GB_7500MTPS')

    Returns:
        List of dicts with curve info and detailed changes
    """
    history = []

    # Normalize config for matching (remove MTPS suffix variations)
    config_base = config.replace("MTPS", "").replace("_", " ").upper()

    for curve in CURVE_ORDER:
        if curve in ELC_CURVES and config in ELC_CURVES[curve]:
            curve_data = ELC_CURVES[curve][config]
            default_target = curve_data.get('default', 0)

            # Get delta info if this isn't the first curve
            idx = CURVE_ORDER.index(curve)
            delta_key = f"{CURVE_ORDER[idx-1]}→{curve}" if idx > 0 else None

            # Get detailed changes for this config
            detailed_changes = []
            if delta_key and delta_key in CURVE_DELTAS:
                delta_data = CURVE_DELTAS[delta_key]
                for change in delta_data.get("detailed_changes", []):
                    # Match config (flexible matching)
                    change_config = change["config"].replace("_", " ").upper()
                    if config_base.startswith(change_config.split()[0]):
                        # Check if this change is for our specific config
                        if change_config in config_base or config_base.startswith(change_config):
                            detailed_changes.append(change)

            history.append({
                "curve": curve,
                "is_active": curve == ACTIVE_CURVE,
                "description": CURVE_INFO[curve]["description"],
                "detailed_changes": detailed_changes,
            })

    return history


def get_all_changes_for_curve_transition(from_curve: str, to_curve: str) -> list[dict]:
    """
    Get all detailed changes between two curves.

    Args:
        from_curve: Previous curve (e.g., 'D0')
        to_curve: New curve (e.g., 'D1')

    Returns:
        List of detailed change dicts
    """
    delta_key = f"{from_curve}→{to_curve}"
    if delta_key in CURVE_DELTAS:
        return CURVE_DELTAS[delta_key].get("detailed_changes", [])
    return []


def get_available_configs_for_curve(curve: str) -> list[str]:
    """Get list of configs available for a specific curve."""
    if curve not in ELC_CURVES:
        return []
    return list(ELC_CURVES[curve].keys())
