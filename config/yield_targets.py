def get_target(targets_dict: dict, did: str, density: str, speed: str, year: int, month: int) -> float | None:
    """
    Get target for a specific DID+Density+Speed combination at a given month.

    Args:
        targets_dict: HMFN_TARGETS, SLT_TARGETS, or ELC_TARGETS
        did: Design ID (e.g., 'Y6CP')
        density: Module density (e.g., '192GB')
        speed: Module speed (e.g., '7500MTPS')
        year: Calendar year
        month: Calendar month (1-12)

    Returns:
        Target percentage or None if not found
    """
    key = f"{did}_{density}_{speed}"

    if key not in targets_dict:
        return None

    schedule = targets_dict[key]

    if (year, month) in schedule:
        return schedule[(year, month)]

    return schedule.get('default')


def get_available_configs(targets_dict: dict) -> list[str]:
    """Get list of available DID+Density+Speed configurations."""
    return list(targets_dict.keys())


def normalize_speed(speed: str) -> str:
    """Normalize speed format (e.g., '7500' -> '7500MTPS')."""
    speed = speed.upper().strip()
    if not speed.endswith('MTPS'):
        speed = f"{speed}MTPS"
    return speed


def normalize_density(density: str) -> str:
    """Normalize density format (e.g., '192gb' -> '192GB')."""
    return density.upper().strip()
